    private void setDataJurusan(ArrayList<Jurusan>dataJurusan) {
        if(dataJurusan == null) {
            JOptionPane.showMessageDialog(this, "Data tidak bisa diload", "Gagal Koneksi",
                   JOptionPane.ERROR_MESSAGE);
        } else {
            tblModel = new DefaultTableModel();
            tblModel.setColumnIdentifiers(new String[] {"Kode Dosen",
                "Nama Dosen"});
            for(Jurusan j : dataJurusan) {
                Object[] o = new Object[3];
                o[0] = j.getKodeJur();
                o[1] = j.getNamaJur();
                tblModel.addRow(o);
            }
            
            tblJurusan.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblJurusan.setModel(tblModel);
        }
    }
    
    private void resetForm() {
        txtKodeJur.setText(null);
        txtNamaJur.setText(null);
        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
        btnSimpan.setEnabled(true);
        txtKodeJur.requestFocus();
    }
    
    private void tableToForm() {
        txtKodeJur.setText(tblModel.getValueAt(tblJurusan.getSelectedRow(), 0) + "");
        txtNamaJur.setText(tblModel.getValueAt(tblJurusan.getSelectedRow(), 1) + "");
        txtKodeJur.setEnabled(false);
        btnSimpan.setEnabled(false);
        btnUbah.setEnabled(true);
        btnHapus.setEnabled(true);
    }

