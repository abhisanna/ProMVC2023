    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtKodeJur.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Kode Jurusan Tidak Boleh Kosong");
            txtKodeJur.requestFocus();
        } else if(txtNamaJur.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama Jurusan Tidak Boleh Kosong");
            txtNamaJur.requestFocus();
        } else {
            String kode = txtKodeJur.getText();
            String nama = txtNamaJur.getText();
            Jurusan j = new Jurusan(kode, nama);
            jController.setDml(j, CRUD.INPUT);
            JOptionPane.showMessageDialog(this, " Data Berhasil di Simpan");
            setDataJurusan(jController.ambilSemuaDataJurusan());
            resetForm();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String kode = txtKodeJur.getText();
        Jurusan j = new Jurusan(kode);
        int hapus = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", null, JOptionPane.YES_NO_OPTION);
        if(hapus == JOptionPane.YES_OPTION) {
            jController.setDml(j, CRUD.HAPUS);
            JOptionPane.showMessageDialog(this, "Data berhasil di hapus");
        }
        
        setDataJurusan(jController.ambilSemuaDataJurusan());
        resetForm();
        
        txtKodeJur.setEnabled(true);
    }//GEN-LAST:event_btnHapusActionPerformed

